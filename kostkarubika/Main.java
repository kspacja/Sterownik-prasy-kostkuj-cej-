    public static Color Background(String c){
        Color color = null;
        if (c.equals("RED"))
            color = Color.RED;
        if (c.equals("WHITE"))
            color = Color.WHITE;
        if (c.equals("BLUE"))
            color = Color.BLUE;
        if (c.equals("GREEN"))
            color = Color.GREEN;
        if (c.equals("YELLOW"))
            color = Color.YELLOW;
        return color;
    }

